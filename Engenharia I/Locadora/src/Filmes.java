
public class Filmes {

	private String nome;
	private Especificacao Especificacao;
	
    public Filmes(String nome, Especificacao Especificacao) {
        this.nome = nome;
        this.Especificacao = Especificacao;
    }
	
	
	public Especificacao getEspecifacacao() {
		return getEspecifacacao();
	}
	
	public void setEspecifacacao(Especificacao especifacacao) {
		Especificacao = especifacacao;
	}
	
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	



	
	

	

	
}
